package 项目.danei.src.first.day01; //包 包名 ---> X号楼 + X单元

public class HelloWorld { // 类名 HelloWorld ---> 门牌号
    /*
     * main方法的快捷键   main回车即可自动生成
     * main方法 ---> 程序的入口
     * 代码执行顺序为逐句往下执行
     */
    public static void main(String[] args) { //大门 入口
        /*
         * 打印信息功能快捷键 ---> sout回车即可
         * println功能 ---> 打印信息并显示在下方窗口中的功能
         *
         * 1.Java严格区分大小写!
         * 2.写代码期间,输入法始终保持在英文输入状态下
         * 3.每句话写完后,要以分号(英文的)结尾
         */
        System.out.println("hello world!");
        System.out.println(3);
        System.out.println(2);
        System.out.println(1);
    } //执行到这里,代表出口(程序结束)
}
